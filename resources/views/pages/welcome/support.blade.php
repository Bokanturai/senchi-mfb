<script>
    // FAQ toggle behavior
    function toggleFaq(button) {
        const item = button.closest('.faq-item');
        const content = item.querySelector('.faq-content');
        const icon = item.querySelector('.faq-icon');
        const iconInner = icon.querySelector('i');
        const isOpen = !content.classList.contains('max-h-0') && content.style.maxHeight !== '0px' && content.style.maxHeight !== '';
        
        // Close all other FAQs first for a cleaner accordion effect
        document.querySelectorAll('.faq-item').forEach(otherItem => {
            if (otherItem !== item) {
                const otherContent = otherItem.querySelector('.faq-content');
                const otherIcon = otherItem.querySelector('.faq-icon');
                otherContent.style.maxHeight = '0px';
                otherContent.classList.add('max-h-0');
                otherItem.classList.remove('border-primary/20', 'bg-white');
                otherItem.classList.add('border-brand-dark/5', 'bg-brand-cream/30');
                otherIcon.classList.remove('rotate-45', 'bg-primary', 'text-white');
                otherIcon.classList.add('bg-white', 'text-brand-dark');
                otherIcon.querySelector('i').setAttribute('data-lucide', 'plus');
            }
        });

        if (isOpen) {
            // Close active FAQ
            content.style.maxHeight = '0px';
            content.classList.add('max-h-0');
            item.classList.remove('border-primary/20', 'bg-white');
            item.classList.add('border-brand-dark/5', 'bg-brand-cream/30');
            icon.classList.remove('rotate-45', 'bg-primary', 'text-white');
            icon.classList.add('bg-white', 'text-brand-dark');
        } else {
            // Open active FAQ
            content.classList.remove('max-h-0');
            content.style.maxHeight = content.scrollHeight + 'px';
            item.classList.add('border-primary/20', 'bg-white');
            item.classList.remove('border-brand-dark/5', 'bg-brand-cream/30');
            icon.classList.add('rotate-45', 'bg-primary', 'text-white');
            icon.classList.remove('bg-white', 'text-brand-dark');
        }
        
        // Reinitialize icons if any changed
        lucide.createIcons();
    }

    // Handle AJAX Submission for Support Inquiry Form
    document.addEventListener('DOMContentLoaded', () => {
        const form = document.getElementById('support-inquiry-form');
        const successAlert = document.getElementById('support-success-alert');
        const alertMsg = document.getElementById('support-success-message');
        const alertRef = document.getElementById('support-success-ref');
        const submitBtn = document.getElementById('support-submit-btn');

        if (form) {
            form.addEventListener('submit', async (e) => {
                e.preventDefault();
                
                // Set loading state
                submitBtn.disabled = true;
                submitBtn.innerHTML = `<span class="flex h-5 w-5 relative shrink-0"><span class="animate-ping absolute inline-flex h-full w-full rounded-full bg-white opacity-75"></span><span class="relative inline-flex rounded-full h-5 w-5 bg-white"></span></span> Processing Request...`;
                
                try {
                    const formData = new FormData(form);
                    const response = await fetch(form.action, {
                        method: 'POST',
                        headers: {
                            'X-Requested-With': 'XMLHttpRequest',
                            'Accept': 'application/json'
                        },
                        body: formData
                    });
                    
                    const result = await response.json();
                    
                    if (response.ok && result.success) {
                        // Success
                        form.reset();
                        successAlert.classList.remove('hidden');
                        alertMsg.textContent = result.message;
                        alertRef.textContent = `TICKET ID: ${result.ref_code}`;
                        successAlert.scrollIntoView({ behavior: 'smooth', block: 'center' });
                        
                        // Reinitialize icons inside alert
                        lucide.createIcons();
                    } else {
                        // Validation errors or exception
                        alert(result.message || 'Something went wrong. Please check your form values.');
                    }
                } catch (err) {
                    console.error(err);
                    // Fallback to native submission if AJAX fails
                    form.submit();
                } finally {
                    // Restore submit button
                    submitBtn.disabled = false;
                    submitBtn.innerHTML = `<span>Submit Request</span> <i data-lucide="send" class="w-4 h-4"></i>`;
                    lucide.createIcons();
                }
            });
        }
    });
</script>
